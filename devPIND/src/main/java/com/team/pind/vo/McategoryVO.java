package com.team.pind.vo;

public class McategoryVO {

	String pind_mcategory_Music = "";
	String pind_mcategory_Sports = "";
	String pind_mcategory_Movie = "";
	String pind_mcategory_Drama = "";
	String pind_mcategory_News = "";
	
	public McategoryVO() {
	}

	public McategoryVO(String pind_mcategory_Music, String pind_mcategory_Sports, String pind_mcategory_Movie,
			String pind_mcategory_Drama, String pind_mcategory_News) {
		super();
		this.pind_mcategory_Music = pind_mcategory_Music;
		this.pind_mcategory_Sports = pind_mcategory_Sports;
		this.pind_mcategory_Movie = pind_mcategory_Movie;
		this.pind_mcategory_Drama = pind_mcategory_Drama;
		this.pind_mcategory_News = pind_mcategory_News;
	}

	public String getPind_mcategory_Music() {
		return pind_mcategory_Music;
	}

	public void setPind_mcategory_Music(String pind_mcategory_Music) {
		this.pind_mcategory_Music = pind_mcategory_Music;
	}

	public String getPind_mcategory_Sports() {
		return pind_mcategory_Sports;
	}

	public void setPind_mcategory_Sports(String pind_mcategory_Sports) {
		this.pind_mcategory_Sports = pind_mcategory_Sports;
	}

	public String getPind_mcategory_Movie() {
		return pind_mcategory_Movie;
	}

	public void setPind_mcategory_Movie(String pind_mcategory_Movie) {
		this.pind_mcategory_Movie = pind_mcategory_Movie;
	}

	public String getPind_mcategory_Drama() {
		return pind_mcategory_Drama;
	}

	public void setPind_mcategory_Drama(String pind_mcategory_Drama) {
		this.pind_mcategory_Drama = pind_mcategory_Drama;
	}

	public String getPind_mcategory_News() {
		return pind_mcategory_News;
	}

	public void setPind_mcategory_News(String pind_mcategory_News) {
		this.pind_mcategory_News = pind_mcategory_News;
	}
	
	

}
